import os
import zipfile
from tqdm import tqdm

# Paths
SOURCE_DIR = '/content/drive/MyDrive/FathomNet/train_rois_final'
ZIP_OUT = '/content/drive/MyDrive/FathomNet/train_rois.zip'

# Get list of all files to zip
files_to_zip = []
for root, dirs, files in os.walk(SOURCE_DIR):
    for file in files:
        files_to_zip.append(os.path.join(root, file))

print(f"📦 Total files to compress: {len(files_to_zip)}")

# Create the Zip with a progress bar
with zipfile.ZipFile(ZIP_OUT, 'w', zipfile.ZIP_STORED) as zipf:
    for file in tqdm(files_to_zip, desc="Zipping ROIs"):
        # arcname ensures the zip doesn't contain the full /content/drive... path
        arcname = os.path.relpath(file, SOURCE_DIR)
        zipf.write(file, arcname)

print(f"\n✅ Zip complete! Saved to: {ZIP_OUT}")
