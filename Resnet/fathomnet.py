import os
import json
import asyncio
import httpx
from PIL import Image
from tqdm.asyncio import tqdm_asyncio
from google.colab import drive

# --- 1. SETUP PATHS ---
DRIVE_BASE = '/content/drive/MyDrive/FathomNet'
JSON_PATH = os.path.join(DRIVE_BASE, 'dataset_train.json')
# We save CROPS to Drive, but download RAW to Local SSD
LOCAL_RAW_DIR = '/content/raw_tmp'
DRIVE_ROI_DIR = os.path.join(DRIVE_BASE, 'train_rois_final')

os.makedirs(LOCAL_RAW_DIR, exist_ok=True)
os.makedirs(DRIVE_ROI_DIR, exist_ok=True)

# --- 2. LOAD DATA ---
with open(JSON_PATH, 'r') as f:
    data = json.load(f)

image_map = {img['id']: img for img in data['images']}
annotations = data['annotations']

# --- 3. DOWNLOAD & CROP LOGIC ---
semaphore = asyncio.Semaphore(10) # Limit concurrent downloads to 10

async def process_entry(client, ann):
    async with semaphore:
        img_info = image_map.get(ann['image_id'])
        if not img_info: return

        url = img_info['coco_url']
        local_path = os.path.join(LOCAL_RAW_DIR, img_info['file_name'])
        roi_name = f"{ann['image_id']}_{ann['id']}.png"
        roi_path = os.path.join(DRIVE_ROI_DIR, roi_name)

        # Skip if ROI already exists on Drive
        if os.path.exists(roi_path): return

        try:
            # Download raw image if not already local
            if not os.path.exists(local_path):
                resp = await client.get(url, timeout=30.0)
                if resp.status_code == 200:
                    with open(local_path, "wb") as f:
                        f.write(resp.content)

            # Crop and save directly to Drive
            if os.path.exists(local_path):
                with Image.open(local_path) as img:
                    x, y, w, h = ann['bbox']
                    if w > 0 and h > 0:
                        crop = img.crop((int(x), int(y), int(x + w), int(y + h)))
                        crop.save(roi_path)

                # OPTIONAL: Delete local raw image to save space as we go
                # os.remove(local_path)
        except Exception:
            pass

async def main():
    limits = httpx.Limits(max_keepalive_connections=5, max_connections=10)
    async with httpx.AsyncClient(limits=limits) as client:
        tasks = [process_entry(client, ann) for ann in annotations]
        await tqdm_asyncio.gather(*tasks, desc="Downloading & Cropping")

# --- 4. EXECUTE ---
import nest_asyncio
nest_asyncio.apply()
asyncio.run(main())

